"""
Physics-Informed Neural Network (PINN) for 1D Burgers' Equation
Generated by IBM Bob (watsonx.ai)

Problem Setup:
    PDE: u_t + u·u_x = ν·u_xx
    Domain: x ∈ [-1, 1], t ∈ [0, 1]
    Viscosity: ν = 0.01/π
    IC: u(x, 0) = -sin(πx)
    BC: u(-1, t) = u(1, t) = 0

Training: Two-phase optimization (Adam + L-BFGS)
Validation: Comparison against Cole-Hopf analytical solution
"""

import torch
import torch.nn as nn
import numpy as np
import matplotlib.pyplot as plt
from scipy.integrate import odeint
from scipy.interpolate import interp1d
import json
import time
from pathlib import Path


# Set random seeds for reproducibility
torch.manual_seed(42)
np.random.seed(42)

# Device configuration
device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
print(f"Using device: {device}")


class BurgersPINN(nn.Module):
    """
    Physics-Informed Neural Network for Burgers' equation.
    
    Architecture: Fully connected network with tanh activation
    Input: (x, t) coordinates
    Output: u(x, t) velocity field
    """
    
    def __init__(self, layers=[2, 64, 64, 64, 64, 1]):
        """
        Args:
            layers: List defining network architecture
                    [input_dim, hidden1, hidden2, ..., output_dim]
        """
        super(BurgersPINN, self).__init__()
        
        self.layers = nn.ModuleList()
        for i in range(len(layers) - 1):
            self.layers.append(nn.Linear(layers[i], layers[i+1]))
        
        # Xavier initialization
        for layer in self.layers:
            nn.init.xavier_normal_(layer.weight)
            nn.init.zeros_(layer.bias)
    
    def forward(self, x, t):
        """
        Forward pass through the network.
        
        Args:
            x: Spatial coordinate tensor
            t: Temporal coordinate tensor
        
        Returns:
            u: Predicted velocity field
        """
        inputs = torch.cat([x, t], dim=1)
        
        for i, layer in enumerate(self.layers[:-1]):
            inputs = torch.tanh(layer(inputs))
        
        u = self.layers[-1](inputs)
        return u


class BurgersEquationSolver:
    """
    Complete PINN solver for Burgers' equation with training and evaluation.
    """
    
    def __init__(self, nu=0.01/np.pi):
        """
        Args:
            nu: Kinematic viscosity coefficient
        """
        self.nu = nu
        self.model = BurgersPINN().to(device)
        
        # Training history
        self.loss_history = {
            'total': [],
            'pde': [],
            'ic': [],
            'bc': [],
            'iteration': []
        }
        
        # Domain bounds
        self.x_min, self.x_max = -1.0, 1.0
        self.t_min, self.t_max = 0.0, 1.0
    
    def generate_training_data(self, n_pde=10000, n_ic=256, n_bc=256):
        """
        Generate collocation points for PDE, IC, and BC.
        
        Args:
            n_pde: Number of interior collocation points
            n_ic: Number of initial condition points
            n_bc: Number of boundary condition points
        
        Returns:
            Dictionary containing training data tensors
        """
        # PDE collocation points (interior domain)
        x_pde = torch.rand(n_pde, 1, device=device) * 2 - 1  # [-1, 1]
        t_pde = torch.rand(n_pde, 1, device=device)          # [0, 1]
        
        # Initial condition points (t=0)
        x_ic = torch.rand(n_ic, 1, device=device) * 2 - 1
        t_ic = torch.zeros(n_ic, 1, device=device)
        u_ic = -torch.sin(np.pi * x_ic)
        
        # Boundary condition points (x=-1 and x=1)
        t_bc = torch.rand(n_bc, 1, device=device)
        x_bc_left = torch.full((n_bc, 1), -1.0, device=device)
        x_bc_right = torch.full((n_bc, 1), 1.0, device=device)
        u_bc = torch.zeros(n_bc, 1, device=device)
        
        return {
            'x_pde': x_pde, 't_pde': t_pde,
            'x_ic': x_ic, 't_ic': t_ic, 'u_ic': u_ic,
            'x_bc_left': x_bc_left, 'x_bc_right': x_bc_right,
            't_bc': t_bc, 'u_bc': u_bc
        }
    
    def compute_pde_residual(self, x, t):
        """
        Compute PDE residual: u_t + u·u_x - ν·u_xx = 0
        
        Args:
            x: Spatial coordinates
            t: Temporal coordinates
        
        Returns:
            PDE residual tensor
        """
        x.requires_grad_(True)
        t.requires_grad_(True)
        
        u = self.model(x, t)
        
        # First derivatives
        u_x = torch.autograd.grad(u, x, grad_outputs=torch.ones_like(u),
                                  create_graph=True)[0]
        u_t = torch.autograd.grad(u, t, grad_outputs=torch.ones_like(u),
                                  create_graph=True)[0]
        
        # Second derivative
        u_xx = torch.autograd.grad(u_x, x, grad_outputs=torch.ones_like(u_x),
                                   create_graph=True)[0]
        
        # Burgers' equation residual
        residual = u_t + u * u_x - self.nu * u_xx
        
        return residual
    
    def compute_loss(self, data):
        """
        Compute total loss: PDE + IC + BC components.
        
        Args:
            data: Dictionary containing training data
        
        Returns:
            total_loss, loss_pde, loss_ic, loss_bc
        """
        # PDE loss
        residual = self.compute_pde_residual(data['x_pde'], data['t_pde'])
        loss_pde = torch.mean(residual ** 2)
        
        # Initial condition loss
        u_ic_pred = self.model(data['x_ic'], data['t_ic'])
        loss_ic = torch.mean((u_ic_pred - data['u_ic']) ** 2)
        
        # Boundary condition loss
        u_bc_left = self.model(data['x_bc_left'], data['t_bc'])
        u_bc_right = self.model(data['x_bc_right'], data['t_bc'])
        loss_bc = torch.mean(u_bc_left ** 2) + torch.mean(u_bc_right ** 2)
        
        # Total loss (weighted sum)
        total_loss = loss_pde + loss_ic + loss_bc
        
        return total_loss, loss_pde, loss_ic, loss_bc
    
    def train_adam(self, data, epochs=5000, lr=1e-3, print_every=500):
        """
        Phase 1: Adam optimizer training.
        
        Args:
            data: Training data dictionary
            epochs: Number of training epochs
            lr: Learning rate
            print_every: Print frequency
        """
        optimizer = torch.optim.Adam(self.model.parameters(), lr=lr)
        
        print("\n" + "="*60)
        print("Phase 1: Adam Optimization")
        print("="*60)
        
        start_time = time.time()
        
        for epoch in range(epochs):
            optimizer.zero_grad()
            total_loss, loss_pde, loss_ic, loss_bc = self.compute_loss(data)
            total_loss.backward()
            optimizer.step()
            
            # Record history
            self.loss_history['total'].append(total_loss.item())
            self.loss_history['pde'].append(loss_pde.item())
            self.loss_history['ic'].append(loss_ic.item())
            self.loss_history['bc'].append(loss_bc.item())
            self.loss_history['iteration'].append(epoch)
            
            if (epoch + 1) % print_every == 0:
                elapsed = time.time() - start_time
                print(f"Epoch {epoch+1:5d}/{epochs} | "
                      f"Loss: {total_loss.item():.6e} | "
                      f"PDE: {loss_pde.item():.6e} | "
                      f"IC: {loss_ic.item():.6e} | "
                      f"BC: {loss_bc.item():.6e} | "
                      f"Time: {elapsed:.1f}s")
        
        print(f"\nAdam optimization completed in {time.time() - start_time:.1f}s")
    
    def train_lbfgs(self, data, max_iter=1000, print_every=100):
        """
        Phase 2: L-BFGS optimizer training.
        
        Args:
            data: Training data dictionary
            max_iter: Maximum number of iterations
            print_every: Print frequency
        """
        optimizer = torch.optim.LBFGS(
            self.model.parameters(),
            max_iter=20,
            tolerance_grad=1e-9,
            tolerance_change=1e-12,
            history_size=50,
            line_search_fn='strong_wolfe'
        )
        
        print("\n" + "="*60)
        print("Phase 2: L-BFGS Optimization")
        print("="*60)
        
        start_time = time.time()
        iteration = [len(self.loss_history['total'])]
        
        def closure():
            optimizer.zero_grad()
            total_loss, loss_pde, loss_ic, loss_bc = self.compute_loss(data)
            total_loss.backward()
            
            # Record history
            self.loss_history['total'].append(total_loss.item())
            self.loss_history['pde'].append(loss_pde.item())
            self.loss_history['ic'].append(loss_ic.item())
            self.loss_history['bc'].append(loss_bc.item())
            self.loss_history['iteration'].append(iteration[0])
            iteration[0] += 1
            
            return total_loss
        
        for i in range(max_iter // 20):
            optimizer.step(closure)
            
            if (i + 1) % (print_every // 20) == 0:
                total_loss = self.loss_history['total'][-1]
                loss_pde = self.loss_history['pde'][-1]
                loss_ic = self.loss_history['ic'][-1]
                loss_bc = self.loss_history['bc'][-1]
                elapsed = time.time() - start_time
                
                print(f"Iter {iteration[0]:5d} | "
                      f"Loss: {total_loss:.6e} | "
                      f"PDE: {loss_pde:.6e} | "
                      f"IC: {loss_ic:.6e} | "
                      f"BC: {loss_bc:.6e} | "
                      f"Time: {elapsed:.1f}s")
        
        print(f"\nL-BFGS optimization completed in {time.time() - start_time:.1f}s")
    
    def cole_hopf_solution(self, x, t):
        """
        Analytical solution via Cole-Hopf transformation.
        
        The Cole-Hopf transformation converts Burgers' equation to the heat equation.
        For IC u(x,0) = -sin(πx), we solve using numerical integration of the
        heat kernel convolution.
        
        Args:
            x: Spatial coordinate (scalar or array)
            t: Temporal coordinate (scalar or array)
        
        Returns:
            u: Analytical solution
        """
        from scipy import integrate
        
        # Handle scalar inputs
        x = np.asarray(x)
        t = np.asarray(t)
        scalar_input = x.ndim == 0
        
        if scalar_input:
            x = x.reshape(1)
            t = t.reshape(1)
        
        # Flatten for processing
        x_flat = x.flatten()
        t_flat = t.flatten()
        u_flat = np.zeros_like(x_flat)
        
        for i, (xi, ti) in enumerate(zip(x_flat, t_flat)):
            if ti == 0:
                # Initial condition
                u_flat[i] = -np.sin(np.pi * xi)
            else:
                # Cole-Hopf solution via heat kernel convolution
                def integrand_phi(eta):
                    # Initial potential: φ_0(η) = exp(-1/(2ν) ∫_0^η u(s,0)ds)
                    # ∫_0^η -sin(πs)ds = [cos(πs)/π]_0^η = (cos(πη)-1)/π
                    integral_u0 = (np.cos(np.pi * eta) - 1.0) / np.pi
                    phi0 = np.exp(-integral_u0 / (2.0 * self.nu))
                    # Heat kernel: G(xi-eta, ti)
                    kernel = np.exp(-((xi - eta) ** 2) / (4.0 * self.nu * ti))
                    return phi0 * kernel
                
                def integrand_dphi(eta):
                    # ∂/∂x [G(xi-eta,ti)] = -(xi-eta)/(2νti) * G
                    integral_u0 = (np.cos(np.pi * eta) - 1.0) / np.pi
                    phi0 = np.exp(-integral_u0 / (2.0 * self.nu))
                    kernel = np.exp(-((xi - eta) ** 2) / (4.0 * self.nu * ti))
                    d_kernel = -(xi - eta) / (2.0 * self.nu * ti) * kernel
                    return phi0 * d_kernel
                
                # Numerical integration over domain [-1, 1]
                phi, _ = integrate.quad(integrand_phi, -1.0, 1.0, limit=200)
                dphi, _ = integrate.quad(integrand_dphi, -1.0, 1.0, limit=200)
                
                # Cole-Hopf formula: u = -2ν * (∂φ/∂x) / φ
                u_flat[i] = -2.0 * self.nu * dphi / (phi + 1e-10)
        
        # Reshape to original shape
        u = u_flat.reshape(x.shape)
        
        if scalar_input:
            u = u.item()
        
        return u
    
    def evaluate(self, n_test=100):
        """
        Evaluate PINN against analytical solution.
        
        Args:
            n_test: Number of test points per dimension
        
        Returns:
            Dictionary containing evaluation metrics and data
        """
        self.model.eval()
        
        # Generate test grid
        x_test = np.linspace(self.x_min, self.x_max, n_test)
        t_test = np.linspace(self.t_min, self.t_max, n_test)
        X, T = np.meshgrid(x_test, t_test)
        
        # Flatten for network input
        x_flat = torch.tensor(X.flatten()[:, None], dtype=torch.float32, device=device)
        t_flat = torch.tensor(T.flatten()[:, None], dtype=torch.float32, device=device)
        
        # PINN prediction
        with torch.no_grad():
            u_pred = self.model(x_flat, t_flat).cpu().numpy()
        u_pred = u_pred.reshape(X.shape)
        
        # Analytical solution
        u_exact = self.cole_hopf_solution(X.flatten(), T.flatten()).reshape(X.shape)
        
        # Compute relative L2 error
        l2_error = np.linalg.norm(u_pred - u_exact) / np.linalg.norm(u_exact)
        
        # Compute pointwise absolute error
        abs_error = np.abs(u_pred - u_exact)
        
        print("\n" + "="*60)
        print("Evaluation Results")
        print("="*60)
        print(f"Relative L² Error: {l2_error:.6e}")
        print(f"Max Absolute Error: {abs_error.max():.6e}")
        print(f"Mean Absolute Error: {abs_error.mean():.6e}")
        
        return {
            'x': x_test,
            't': t_test,
            'X': X,
            'T': T,
            'u_pred': u_pred,
            'u_exact': u_exact,
            'abs_error': abs_error,
            'l2_error': l2_error
        }
    
